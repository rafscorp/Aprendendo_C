#include <stdio.h>

int main() {
    int i = 1; // inicializamos a variável do contador

    // Enquanto i for menor ou igual a 10, continue executando o loop
    while (i <= 10) {
        printf("%d\n", i); // imprime o valor atual de i
        i++; // incrementa i em 1 a cada iteração
    }

    return 0;
}
